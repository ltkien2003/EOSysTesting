package com.eos.model;

import com.eos.untils.XDate;
import java.util.Date;

/**
 *
 * @author Kienltpc04639
 */
public class KhoaHoc {

    private int maKH;
    private String tenKH;
    private double hocPhi;
    private Date ngayKG;
    private String ghiChu;
    private String maNV;
    private Date ngayTao = XDate.now();
    private int tongSoChuong;
    private String hinh;

    @Override
    public String toString() {
        return this.tenKH;
    }

    public int getMaKH() {
        return maKH;
    }

    public void setMaKH(int maKH) {
        this.maKH = maKH;
    }

    public String getTenKH() {
        return tenKH;
    }

    public void setTenKH(String tenKH) {
        this.tenKH = tenKH;
    }

    public double getHocPhi() {
        return hocPhi;
    }

    public void setHocPhi(double hocPhi) {
        this.hocPhi = hocPhi;
    }

    public Date getNgayKG() {
        return ngayKG;
    }

    public void setNgayKG(Date ngayKG) {
        this.ngayKG = ngayKG;
    }

    public String getGhiChu() {
        return ghiChu;
    }

    public void setGhiChu(String ghiChu) {
        this.ghiChu = ghiChu;
    }

    public String getMaNV() {
        return maNV;
    }

    public void setMaNV(String maNV) {
        this.maNV = maNV;
    }

    public Date getNgayTao() {
        return ngayTao;
    }

    public void setNgayTao(Date ngayTao) {
        this.ngayTao = ngayTao;
    }

    public int getTongSoChuong() {
        return tongSoChuong;
    }

    public void setTongSoChuong(int tongSoChuong) {
        this.tongSoChuong = tongSoChuong;
    }

    public String getHinh() {
        return hinh;
    }

    public void setHinh(String hinh) {
        this.hinh = hinh;
    }

}
